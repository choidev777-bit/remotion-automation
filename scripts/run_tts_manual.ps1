$results = @()

for ($i = 0; $i -lt $narrations.Count; $i++) {
  $ttsJobId = "$jobId-scene-$i"
  $text = $narrations[$i]
  
  Write-Host "[$i/24] TTS 생성 중 (jobId: $ttsJobId)..." -ForegroundColor Cyan
  Write-Host "  텍스트: $($text.Substring(0, [Math]::Min(40, $text.Length)))..." -ForegroundColor Gray
  
  try {
    $body = @{ text = $text; jobId = $ttsJobId } | ConvertTo-Json -Compress
    $response = Invoke-RestMethod -Uri "$baseUrl/api/tts" `
      -Method POST `
      -ContentType "application/json; charset=utf-8" `
      -Body ([System.Text.Encoding]::UTF8.GetBytes($body)) `
      -TimeoutSec 300
    
    Write-Host "  ✅ 완료: $($response.audioSrc) (${response.duration}초)" -ForegroundColor Green
    $results += @{ idx = $i; audioSrc = $response.audioSrc; duration = $response.duration; success = $true }
  } catch {
    Write-Host "  ❌ 실패: $_" -ForegroundColor Red
    $results += @{ idx = $i; audioSrc = $null; duration = 0; success = $false }
  }
}

Write-Host ""
Write-Host "=== TTS 완료 ===" -ForegroundColor Yellow
$success = ($results | Where-Object { $_.success }).Count
Write-Host "성공: $success / $($narrations.Count)" -ForegroundColor Green

# 결과를 JSON으로 저장
$results | ConvertTo-Json -Depth 3 | Out-File -FilePath ".\scripts\tts_results.json" -Encoding UTF8
Write-Host "결과 저장: scripts\tts_results.json" -ForegroundColor Cyan
